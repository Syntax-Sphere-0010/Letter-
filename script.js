const speed = 50;
let i = 0;

function typeWriter() {
    if (i < text.length) {
        document.getElementById("typewriter").innerHTML += text.charAt(i);
        i++;
        // Auto scroll to bottom
        const elem = document.getElementById("typewriter").parentElement.parentElement;
        if (elem) elem.scrollTop = elem.scrollHeight;
        setTimeout(typeWriter, speed);
    }
}

document.addEventListener('DOMContentLoaded', function () {
    const startBtn = document.getElementById('startBtn');
    const textContainer = document.getElementById('textContainer');
    const wrapper = startBtn.parentElement;

    if (startBtn) {
        startBtn.addEventListener('click', function () {
            startBtn.classList.add('hidden');
            textContainer.classList.remove('hidden');

            // Change alignment
            wrapper.classList.remove('items-center', 'justify-center');
            wrapper.classList.add('items-start', 'justify-start');

            typeWriter();
        });
    }
});
